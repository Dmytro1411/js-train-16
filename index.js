/**
 * Функція `buildMatrix` створює двовимірний масив (матрицю) розміром size x size,
 * заповнює її випадковими числами від 0 до 9, а потім виводить матрицю в консоль
 *
 *  size - розмір матриці.
 *  Повернеться згенерована матриця.
 */
function buildMatrix(size) {
  // Перевіряємо, чи size є числом.
  if (typeof size !== "number") {
    console.error("Аргумент має бути числом!"); // Якщо size не є числом, виведеме в консоль критичну помилку з текстом "Аргумент має бути числом!".
    return null; // Повертаємо null, що свідчить про неможливість обробки вхідних даних.
  } else {
    let arr = []; // Створюємо порожній масив який буде нашою матрицею.
    // Цикл для заповнення рядків матриці, лічильник і має бути від 0 до розміру матриці.
    for (let i = 0; i < size; i++) {
      let arrString = []; // Створюємо порожній масив, який буде рядком матриці.
      // Цикл всередені циклу для заповнення колонок матриці,лічильник j має бути від 0 до розміру матриці.
      for (let j = 0; j < size; j++) {
        // От 0 до 9
        let generatorRandom = Math.trunc(Math.random() * 10); // Генеруємо випадкове число від 0 до 9 округляємо його до меншого цілого, і додаємо його до рядка.
        // log(generatorRandom);
        arrString.push(generatorRandom);
      }
      arr.push(arrString); // Додаємо рядок до матриці.
    }
    console.table(arr); // Виводимо матрицю в консоль за допомогою як таблицю.
    return arr; // Повертаємо створену матрицю.
  }
}

console.log("Завдання: 5 ==============================");
buildMatrix(5);
// Виведе в консоль матрицю розміром 5x5, заповнену випадковими числами від 0 до 9.

