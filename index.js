/**
 * Функція `calculateTotalPrice` обчислює загальну вартість товарів та логує час виконання.
 *
 * products - Масив об'єктів товарів з властивостями `price` та `title`.
 */
function calculateTotalPrice(products) {
  console.time("timerCalculetorTotalPrice"); // Початок вимірювання часу

  let total = 0; // Створення змінної total яка буде нашою загальную ціною, початкове значення нуль

  // Перебираємо кожен об'єкт товару та...
  products.forEach((item) => {
    // log(item.price);
    total += item.price; // ...додаємо ціну товару до загальної вартості
  });

  log("Загальна вартість товарів:", total); // Виведення загальної вартості товарів у форматі "Загальна вартість товарів:", total
  console.timeEnd("timerCalculetorTotalPrice"); // Зупинка вимірювання та виведення часу
}

